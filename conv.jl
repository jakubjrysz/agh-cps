using LinearAlgebra

function convolution(x, h)
    n = length(x)
    m = length(h)
    y = zeros(n + m - 1)
    for i in 1:n
        for j in 1:m
            y[i + j - 1] += x[i] * h[j]
        end
    end
    return y
end


